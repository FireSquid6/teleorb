export class LevelTilemap extends ex.TileMap {
  constructor() {
    super({
      tileWidth: TILE_SIZE,
      tileHeight: TILE_SIZE,
      columns: COLS,
      rows: ROWS,
    });
  }

  override onInitialize(_engine: ex.Engine): void {
    for (let row = 0; row < ROWS; row++) {
      for (let col = 0; col < COLS; col++) {
        const tile = this.getTile(col, row);

        if (tile === null) {
          throw new Error(`Couldn't find tile 1, 1`);
        }

        if (DEFAULT_LEVEL[row][col] === 1) {
          tile.solid = true;
          const sprite = Tilesets.GridTileset.getSprite(1, 1);
          if (sprite) {
            tile.addGraphic(sprite);
          }
        }
      }
    }
  }
}
